
package String;

import java.util.*;

public class Substring {

    public static void main(String[] args) {
        
        String str = "Java String substring method";
    
        System.out.println(" Original String: " + str);
        System.out.println(" The Substring's: " + str.substring(5, 11)); //g Sub
        System.out.println(" The Substring's: " + str.substring(1)); 
    }
    
}
